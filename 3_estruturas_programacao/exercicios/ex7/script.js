// Inicializa a variável de controle do loop
let x = 0;

// Inicia o loop while
while (x <= 10) {
  // Exibe o valor da variável de controle no console
  console.log(x);
  // Incrementa a variável de controle para evitar um loop infinito
  x++;
}
