// A função Math.pow() exibe a potencia de um número;
// Teste a função com console.log() e as seguintes bases: 2, 3, 18 e o
// expoente deve ser 2;
// Recebe 2 argumentos, base e expoente;
// Ex: Math.pow(5,3);

// Definição do valor do expoente
let expoente = 2;

// Definição dos números de base
let number1 = 2;
let number2 = 3;
let number3 = 18;

// Usando a função Math.pow() para calcular a potência de cada número com o expoente definido
console.log(Math.pow(number1, expoente)); // Calcula 2 elevado à potência de 2 (2^2) e exibe o resultado
console.log(Math.pow(number2, expoente)); // Calcula 3 elevado à potência de 2 (3^2) e exibe o resultado
console.log(Math.pow(number3, expoente)); // Calcula 18 elevado à potência de 2 (18^2) e exibe o resultado
