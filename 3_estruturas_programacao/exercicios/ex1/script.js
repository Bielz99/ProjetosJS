// Armazene em valores em variáveis com cada um dos tipos de dados vistos;
// String, Number e Boolean;

// Declaração e inicialização de uma variável do tipo Boolean
let isOpen = false; // A variável 'isOpen' armazena o valor booleano 'false'

// Declaração e inicialização de uma variável do tipo String
let nameGabriel = "Gabriel"; // A variável 'nameGabriel' armazena a string 'Gabriel'

// Declaração e inicialização de uma variável do tipo Number
let idade = 10; // A variável 'idade' armazena o número '10'

// Verificação do tipo de dado de cada variável usando o operador 'typeof'
console.log(typeof isOpen); // 'boolean' - A variável 'isOpen' é do tipo Boolean
console.log(typeof nameGabriel); // 'string' - A variável 'nameGabriel' é do tipo String
console.log(typeof idade); // 'number' - A variável 'idade' é do tipo Number
